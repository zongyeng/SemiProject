DROP SEQUENCE MEMBERBOARDSEQ;
DROP TABLE MEMBERBOARD;

CREATE SEQUENCE LOGINBOARDSEQ;
--번호, 아이디, 비번, 이름, 주소, 전화번호, 이메일, 가입여부(Y,N), 등급(ADMIN/USER/...)
CREATE TABLE LOGINBOARD(
	MYNO NUMBER NOT NULL,
	MYID VARCHAR2(500) NOT NULL,
	MYPW VARCHAR2(500) NOT NULL,
	MYNAME VARCHAR2(1000) NOT NULL,
	MYADDR VARCHAR2(4000) NOT NULL,
	MYPHONE VARCHAR2(20) NOT NULL,
	MYEMAIL VARCHAR2(100) NOT NULL,
	MYENABLED VARCHAR2(2) NOT NULL,
	MYROLE VARCHAR2(100) NOT NULL,
	CONSTRAINT MYLOGIN_PK PRIMARY KEY(MYNO),
	CONSTRAINT MYLOGIN__UK_ID UNIQUE(MYID),
	CONSTRAINT MYLOGIN_UK_PHONE UNIQUE(MYPHONE),
	CONSTRAINT MYLOGIN_UK_EMAIL UNIQUE(MYEMAIL),
	CONSTRAINT MYLOGIN_CK_ENABLED CHECK(MYENABLED IN ('Y','N'))
);

